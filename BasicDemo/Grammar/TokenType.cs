namespace BasicDemo.Grammar
{
   public enum TokenType
   {
      plus,    // +
      minus,   // -
      mult,    // *
      div,     // /
      equal,   // =
      greater, // >
      less,    // <
      open,    // (
      close,   // )
      number,  // 1, 12, 1.0, 0.2, .4, ...
      let,     // LET
      name,    // x, y, index, ...
   }
}
